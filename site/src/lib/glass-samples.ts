export type GlassSampleCode = keyof typeof GLASS_SAMPLE_TEXT;

const LEGACY_LANGUAGE_CODES: Record<string, string> = {
  in: "id",
  iw: "he",
  ji: "yi",
  jw: "jv",
  fil: "tl",
  tw: "ak",
};

const SCRIPT_SUBTAGS = new Set([
  "arab",
  "brai",
  "cyrl",
  "hans",
  "hant",
  "latn",
  "mong",
]);

export function glassSampleCodeForLanguage(
  value: string | null | undefined,
): GlassSampleCode | null {
  if (!value) return null;
  const normalized = value.trim().replace(/_/g, "-").toLocaleLowerCase();
  if (!normalized) return null;

  const parts = normalized.split("-").filter(Boolean);
  const rawBase = parts[0] ?? "";
  const base = LEGACY_LANGUAGE_CODES[rawBase] ?? rawBase;
  const script = parts.find((part) => SCRIPT_SUBTAGS.has(part));

  const candidates: string[] = [];
  if (base === "zh") {
    candidates.push(script === "hant" ? "zh-Hant" : "zh-Hans");
  }
  if (script) candidates.push(`${base}-${titleCase(script)}`);
  candidates.push(base);

  for (const candidate of candidates) {
    if (hasGlassSampleCode(candidate)) return candidate;
  }
  return null;
}

function hasGlassSampleCode(code: string): code is GlassSampleCode {
  return Object.prototype.hasOwnProperty.call(GLASS_SAMPLE_TEXT, code);
}

function titleCase(value: string) {
  return value.charAt(0).toLocaleUpperCase() + value.slice(1);
}
